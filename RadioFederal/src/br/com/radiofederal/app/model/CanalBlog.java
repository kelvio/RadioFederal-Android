package br.com.radiofederal.app.model;

public class CanalBlog {

	private String nome;

	private String numeroPosts;

	private String url;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getNumeroPosts() {
		return numeroPosts;
	}

	public void setNumeroPosts(String numeroPosts) {
		this.numeroPosts = numeroPosts;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

}
